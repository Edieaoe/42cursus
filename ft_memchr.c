#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*u;

	u = (unsigned char *)s;
	while (n-- > 0)
	{
		if (*u == (unsigned char)c)
			return ((void *)u);
		u++;
	}
	return (NULL);
}
